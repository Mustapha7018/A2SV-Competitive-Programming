def arrayManipulation(n, queries):
    # Write your code here
    diff = [0]  * (n+2)
    for query in queries:
        a = query[0]
        b = query[1]
        c = query[2]
        
        diff[a] += c
        diff[b+1] -= c
    
    for i in range(1, n+2):
        diff[i] += diff[i - 1] 
    
    return max(diff)
    
    
